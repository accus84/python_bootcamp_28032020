#krotka (tupla)
x = (1, 2, "ala", 2.0, "ma kota")
print(x)
print(x.index("ala"))   #na którym miejscu jest element
print(len(x))           #długość tupli
print(len("ala"))       #długość elementu ala w tupli
print(x.count(4))       #policz element 4
print(3 in x)           #czy jest 3 w tupli
print(x[0:3])           #podaj elementt od 0 do 3
